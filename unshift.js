// Metodo unshift
// Adiciona um ou mais elemetos ao início do array

let numeros = [3, 4, 5, 6];

// Utilizando unshift para acrescentar elementos no início do array

numeros.unshift(0, 1, 2);

console.log(numeros);

// [0, 1, 2, 3, 4, 5, 6]


let perfumes = ['egeo', 'coffee', 'floratta', 'eudora'];

perfumes.unshift('lancôme', 'paco rabanne', 'carolina herreira');

console.log(perfumes);

// ['egeo', 'coffee', 'floratta', 'eudora', 'lancôme', 'paco rabanne', 'carolina herreira]
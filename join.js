// Metodo join
// Une os elementos de um array em uma única string

let refrigerantes = ['coca', 'guaraná', 'fanta', 'sprite', 'pepsi'];

// Utilizando o  join para juntar os refrigerantes

let refri = refrigerantes.join(', ');

console.log(refri);

let refrigerantess = refrigerantes.join(' - ');

console.log(refrigerantess); 

// [coca - guaraná - fanta - sprite - pepsi]




// Metodo slice
// Extrair elementos de um array

let marcaRoupas = ['nike', 'adiddas', 'puma', 'lacoste', 'gucci', 'prada'];

let roupas = marcaRoupas.slice(2, 5);

console.log(roupas);

// ['puma', 'lacoste', 'gucci']


let porçõesFritas = ['batata frita', 'isca de peixe', 'coxinha', 'salame', 'pastel', 'mandioca'];

let fritas = porçõesFritas.slice(3, 5);

console.log(fritas);

// ['salame', 'pastel']
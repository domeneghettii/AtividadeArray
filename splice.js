// Metodo splice
// Permite adicionar, remover ou substituir elementos em qualquer posição do array

let frutas = ['banana', 'goiaba', 'amexa', 'manga'];

frutas.splice(3, 1);
// Vai remover o item 

console.log(frutas);

// ['banana', 'goiaba', 'amexa'] 



//] Metodo shift
// Remove o primeiro elemento do array

let livros = ['eu e esse meu coração', 'amanhecer', 'o cérebro de um ansioso', 'a cinco passos de você'];

let primeiroLivro = livros.shift();

// 'eu e esse meu coração'
console.log(primeiroLivro); 

console.log(livros);

// ['amanhecer', 'o cérebro de um ansioso, 'a cinco passos de você']



